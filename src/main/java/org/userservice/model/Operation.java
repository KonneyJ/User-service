package org.userservice.model;

/**
 * Перечисление (enum), определяющее типы операций с пользовательским аккаунтом.
 *
 * Используется для указания характера события, связанного с изменением состояния аккаунта пользователя,
 * в механизмах обмена сообщениями через Kafka и при отправке уведомлений.
 *
 * <p>Возможные значения:</p>
 * <ul>
 *   <li>{@code CREATED} — аккаунт пользователя был создан.</li>
 *   <li>{@code DELETED} — аккаунт пользователя был удалён.</li>
 * </ul>
 *
 * @see UserEvent — класс события, использующий данное перечисление для указания типа операции.
 */
public enum Operation {
    /**
     * Операция создания аккаунта пользователя.
     * Указывает, что пользователь был успешно зарегистрирован в системе.
     */
    CREATED,

    /**
     * Операция удаления аккаунта пользователя.
     * Указывает, что аккаунт пользователя был деактивирован или удалён из системы.
     */
    DELETED
}
